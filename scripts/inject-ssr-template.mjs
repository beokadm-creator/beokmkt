import { readFileSync, writeFileSync, existsSync } from 'fs'
import { resolve, dirname } from 'path'
import { fileURLToPath } from 'url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const rootDir = resolve(__dirname, '..')
const distDir = resolve(rootDir, 'dist')
const functionsDir = resolve(rootDir, 'functions')

const indexHtmlPath = resolve(distDir, 'index.html')
if (!existsSync(indexHtmlPath)) {
  console.error('[inject-ssr-template] dist/index.html not found. Run build:spa first.')
  process.exit(1)
}

const indexHtml = readFileSync(indexHtmlPath, 'utf-8')

// Extract asset paths from the built index.html
const jsMatches = [...indexHtml.matchAll(/src="(\/assets\/[^"]+\.js)"/g)].map((m) => m[1])
const cssMatches = [...indexHtml.matchAll(/href="(\/assets\/[^"]+\.css)"/g)].map((m) => m[1])

const assetPaths = { js: jsMatches, css: cssMatches }

// Build the template: the index.html with a placeholder marker we can use at runtime
// We'll store the full HTML as a template string
const output = `// Auto-generated by scripts/inject-ssr-template.mjs — do not edit manually
// Regenerated after each \`npm run build:spa\`

export const ssrTemplate = ${JSON.stringify(indexHtml)}

export const assetPaths = ${JSON.stringify(assetPaths, null, 2)}
`

const outPath = resolve(functionsDir, 'ssr-template.mjs')
writeFileSync(outPath, output, 'utf-8')
console.log(`[inject-ssr-template] Wrote ${outPath}`)
console.log(`[inject-ssr-template] JS assets: ${jsMatches.join(', ') || '(none)'}`)
console.log(`[inject-ssr-template] CSS assets: ${cssMatches.join(', ') || '(none)'}`)
