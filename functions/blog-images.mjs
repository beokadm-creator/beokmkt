function stableIndex(value, length) {
  if (!length) return 0
  let hash = 0
  const source = String(value || '')
  for (let i = 0; i < source.length; i += 1) {
    hash = ((hash << 5) - hash + source.charCodeAt(i)) | 0
  }
  return Math.abs(hash) % length
}

